package type_casting;

public class TypeCasting {
    public static void main(String[] args) {

//      int->char

        int a = 59;
        char b;
        b=(char)a;
        System.out.println(b);

//       float->char

        float r=299.7F;
        b=(char)r;
        System.out.println(b);

//        float->char->int
        int t;
        t=(int)(char)r;
        System.out.println(t);



    }
    }

